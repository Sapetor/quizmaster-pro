let currentLanguage = 'en';

export function getTranslation(key, ...args) {
    const translation = translations[currentLanguage]?.[key] || translations['en']?.[key] || key;
    
    // Replace placeholders {0}, {1}, etc. with provided arguments
    return translation.replace(/{(\d+)}/g, (match, index) => {
        return args[index] !== undefined ? args[index] : match;
    });
}

export function getOptionLetter(index) {
    const letters = ['option_letter_a', 'option_letter_b', 'option_letter_c', 'option_letter_d'];
    return getTranslation(letters[index] || 'option_letter_a');
}

export function showAlert(translationKey, ...args) {
    alert(getTranslation(translationKey, ...args));
}

export function showConfirm(translationKey, ...args) {
    return confirm(getTranslation(translationKey, ...args));
}

export function translatePage() {
    // Translate elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(element => {
        const translationKey = element.getAttribute('data-translate');
        const args = element.getAttribute('data-translate-args');
        const parsedArgs = args ? args.split(',').map(arg => arg.trim()) : [];
        
        if (element.tagName === 'INPUT' && (element.type === 'text' || element.type === 'number')) {
            element.placeholder = getTranslation(translationKey, ...parsedArgs);
        } else if (element.hasAttribute('data-translate-title')) {
            element.title = getTranslation(translationKey, ...parsedArgs);
        } else if (element.tagName === 'OPTION') {
            element.textContent = getTranslation(translationKey, ...parsedArgs);
        } else if (element.innerHTML.includes('<') && !element.querySelector('[data-translate]')) {
            element.innerHTML = getTranslation(translationKey, ...parsedArgs);
        } else {
            element.textContent = getTranslation(translationKey, ...parsedArgs);
        }
    });
    
    // Translate elements with data-translate-title attribute
    document.querySelectorAll('[data-translate-title]').forEach(element => {
        const translationKey = element.getAttribute('data-translate-title');
        element.title = getTranslation(translationKey);
    });
    
    // Translate placeholders
    document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
        const translationKey = element.getAttribute('data-translate-placeholder');
        element.placeholder = getTranslation(translationKey);
    });
}

export function setLanguage(lang) {
    if (translations[lang]) {
        currentLanguage = lang;
        localStorage.setItem('language', lang);
        
        // Update language selector if it exists
        const selector = document.getElementById('language-selector');
        if (selector) {
            selector.value = lang;
        }
        
        translatePage();
        
        // Also update dynamic game content if in game
        if (window.game && typeof window.game.updateGameTranslations === 'function') {
            window.game.updateGameTranslations();
        }
    }
}

export function getCurrentLanguage() {
    return currentLanguage;
}

// Global function for changing language (called from HTML)
export function changeLanguage(lang) {
    setLanguage(lang);
}

// Helper function for setting translated text in elements
export function setTranslatedText(elementOrId, translationKey, ...args) {
    const element = typeof elementOrId === 'string' ? document.getElementById(elementOrId) : elementOrId;
    if (element) {
        element.textContent = getTranslation(translationKey, ...args);
    }
}

// Make it globally accessible for HTML onclick handlers
window.changeLanguage = changeLanguage;
